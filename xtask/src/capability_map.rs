const SCHEMA_VERSION: u32 = 2;
const TARGET_ITEM_COUNT: usize = 1000;

#[derive(Args, Debug, Clone)]
pub struct CapabilityMapArgs {
  /// Write a human-readable capability map (markdown).
  #[arg(long, default_value = "docs/capability_map.md")]
  pub out_md: PathBuf,

  /// Optionally write a machine-readable capability map (JSON).
  #[arg(long)]
  pub out_json: Option<PathBuf>,
}

#[derive(Debug, Clone, Copy, PartialEq, Eq, PartialOrd, Ord, Serialize)]
#[serde(rename_all = "snake_case")]
enum PriorityTier {
  P0,
  P1,
  P2,
  P3,
}

impl PriorityTier {
  fn label(self) -> &'static str {
    match self {
      Self::P0 => "P0",
      Self::P1 => "P1",
      Self::P2 => "P2",
      Self::P3 => "P3",
    }
  }
}

#[derive(Debug, Clone, Serialize)]
struct CapabilityItem {
  id: String,
  title: String,
  tier: PriorityTier,
  #[serde(skip_serializing_if = "Option::is_none")]
  why: Option<String>,
  #[serde(default, skip_serializing_if = "Vec::is_empty")]
  specs: Vec<String>,
  #[serde(default, skip_serializing_if = "Vec::is_empty")]
  evidence: Vec<String>,
  #[serde(default, skip_serializing_if = "Option::is_none")]
  group: Option<String>,
}

#[derive(Debug, Clone, Serialize)]
struct CapabilityMap {
  schema_version: u32,
  generated_at_commit: Option<String>,
  target_items: usize,
  items: Vec<CapabilityItem>,
}

pub fn run_capability_map(args: CapabilityMapArgs) -> Result<()> {
  let repo_root = crate::repo_root();
  let out_md = resolve_repo_path(&repo_root, &args.out_md);
  let out_json = args
    .out_json
    .as_ref()
    .map(|p| resolve_repo_path(&repo_root, p));

  let items = build_items(&repo_root)?;
  let map = CapabilityMap {
    schema_version: SCHEMA_VERSION,
    generated_at_commit: current_git_sha(&repo_root),
    target_items: TARGET_ITEM_COUNT,
    items,
  };

  write_capability_map_markdown(&map, &out_md)?;
  if let Some(out_json) = out_json {
    write_json(&map, &out_json)?;
  }

  Ok(())
}

fn resolve_repo_path(repo_root: &Path, path: &Path) -> PathBuf {
  if path.is_absolute() {
    return path.to_path_buf();
  }
  repo_root.join(path)
}

fn ensure_parent_dir(path: &Path) -> Result<()> {
  if let Some(parent) = path.parent() {
    fs::create_dir_all(parent)
      .with_context(|| format!("create parent dir {}", parent.display()))?;
  }
  Ok(())
}

fn write_json<T: Serialize>(value: &T, path: &Path) -> Result<()> {
  ensure_parent_dir(path)?;
  let json = serde_json::to_string_pretty(value).context("serialize json")?;
  fs::write(path, json + "\n").with_context(|| format!("write {}", path.display()))?;
  Ok(())
}

fn write_capability_map_markdown(map: &CapabilityMap, path: &Path) -> Result<()> {
  ensure_parent_dir(path)?;

  let mut items = map.items.clone();
  items.sort_by_key(|item| (item.tier, item.why.is_none(), item.id.clone()));

  let mut p0 = 0usize;
  let mut p1 = 0usize;
  let mut p2 = 0usize;
  let mut p3 = 0usize;
  for item in &items {
    match item.tier {
      PriorityTier::P0 => p0 += 1,
      PriorityTier::P1 => p1 += 1,
      PriorityTier::P2 => p2 += 1,
      PriorityTier::P3 => p3 += 1,
    }
  }

  let mut out = String::new();
  out.push_str("# Capability map (prioritized backlog)\n\n");
  out.push_str("This file is generated by `bash scripts/cargo_agent.sh xtask capability-map`.\n\n");
  out.push_str("Principles:\n");
  out.push_str("- Breadth-first (BFS), not depth-first (DFS): keep a frontier across subsystems (HTML/CSS/style/layout/paint/text/resources) instead of going deep on niche features.\n");
  out.push_str("- This is not a global coordination mechanism: if many agents are working, diversify across subsystems and avoid everyone piling onto the same hotspot file.\n");
  out.push_str("- Prefer spec-aligned evidence: WPT reftests, small offline fixtures, and targeted unit/layout/paint tests.\n\n");

  if let Some(commit) = &map.generated_at_commit {
    out.push_str(&format!("- Generated at commit: `{}`\n", commit));
  }
  out.push_str(&format!("- Target items: {}\n", map.target_items));
  out.push_str(&format!(
    "- Counts: P0={} P1={} P2={} P3={}\n\n",
    p0, p1, p2, p3
  ));

  out.push_str("Offline references:\n");
  out.push_str("- WHATWG HTML: `specs/whatwg-html/`\n");
  out.push_str("- CSSWG drafts: `specs/csswg-drafts/`\n\n");

  out.push_str("## P0 (foundational blockers)\n\n");
  write_tier_list(&mut out, &items, PriorityTier::P0);
  out.push_str("\n## P1 (common compounding features)\n\n");
  write_tier_list(&mut out, &items, PriorityTier::P1);
  out.push_str("\n## P2 (important but secondary)\n\n");
  write_tier_list(&mut out, &items, PriorityTier::P2);
  out.push_str("\n## P3 (long tail / niche)\n\n");
  write_tier_list(&mut out, &items, PriorityTier::P3);

  fs::write(path, out).with_context(|| format!("write {}", path.display()))?;
  Ok(())
}

fn write_tier_list(out: &mut String, items: &[CapabilityItem], tier: PriorityTier) {
  for item in items.iter().filter(|item| item.tier == tier) {
    let compact = item.why.is_none() && item.evidence.is_empty() && item.specs.len() <= 1;
    if compact {
      if let Some(spec) = item.specs.first() {
        out.push_str(&format!("- `{}`: {} ({})\n", item.id, item.title, spec));
      } else {
        out.push_str(&format!("- `{}`: {}\n", item.id, item.title));
      }
      continue;
    }

    out.push_str(&format!("- `{}`: {}\n", item.id, item.title));
    if let Some(why) = &item.why {
      out.push_str(&format!("  - why: {}\n", why));
    }
    if !item.specs.is_empty() {
      out.push_str("  - specs:\n");
      for spec in &item.specs {
        out.push_str(&format!("    - {}\n", spec));
      }
    }
    if !item.evidence.is_empty() {
      out.push_str("  - evidence:\n");
      for ev in &item.evidence {
        out.push_str(&format!("    - {}\n", ev));
      }
    }
  }
}

fn current_git_sha(repo_root: &Path) -> Option<String> {
  Command::new("git")
    .args(["rev-parse", "HEAD"])
    .current_dir(repo_root)
    .output()
    .ok()
    .and_then(|out| out.status.success().then_some(out.stdout))
    .and_then(|bytes| String::from_utf8(bytes).ok())
    .map(|s| s.trim().to_string())
    .filter(|s| !s.is_empty())
}

fn build_items(repo_root: &Path) -> Result<Vec<CapabilityItem>> {
  let mut items = Vec::new();

  // Curated keystones (high-signal, with rationale).
  items.extend(curated_items());

  // Algorithm-level backlog slices (break down "implement inline" etc into real work items).
  items.extend(algorithm_items());
  items.extend(csswg_mined_algorithm_items(repo_root)?);
  items.extend(html_mined_algorithm_items(repo_root)?);

  // Broad HTML surface area.
  items.extend(html_items());

  // CSS surface area mined from CSSWG drafts (properties/at-rules/functions; values as filler).
  items.extend(csswg_items(repo_root));

  // Deduplicate by id, keeping the highest priority tier and most informative entry.
  let mut by_id: HashMap<String, CapabilityItem> = HashMap::new();
  for item in items {
    match by_id.get(&item.id) {
      None => {
        by_id.insert(item.id.clone(), item);
      }
      Some(existing) => {
        let should_replace = item.tier < existing.tier
          || (item.tier == existing.tier && existing.why.is_none() && item.why.is_some());
        if should_replace {
          by_id.insert(item.id.clone(), item);
        }
      }
    }
  }

  let mut items: Vec<CapabilityItem> = by_id.into_values().collect();
  items.sort_by_key(|item| (item.tier, item.why.is_none(), item.id.clone()));

  // Keep all curated items (those with a `why`), then fill to target size.
  let must_keep_ids: HashSet<String> = items
    .iter()
    .filter(|item| item.why.is_some() || item.id.starts_with("alg."))
    .map(|item| item.id.clone())
    .collect();

  if items.len() > TARGET_ITEM_COUNT {
    let mut trimmed = Vec::with_capacity(TARGET_ITEM_COUNT);
    for item in &items {
      if must_keep_ids.contains(&item.id) {
        trimmed.push(item.clone());
      }
    }
    trimmed.sort_by_key(|item| (item.tier, item.why.is_none(), item.id.clone()));
    trimmed.truncate(TARGET_ITEM_COUNT);
    for item in &items {
      if trimmed.len() >= TARGET_ITEM_COUNT {
        break;
      }
      if must_keep_ids.contains(&item.id) {
        continue;
      }
      trimmed.push(item.clone());
    }
    trimmed.sort_by_key(|item| (item.tier, item.why.is_none(), item.id.clone()));
    return Ok(trimmed);
  }

  // If the spec scan produced fewer than 1000, add additional CSS value definitions to reach 1000.
  if items.len() < TARGET_ITEM_COUNT {
    let mut fill = csswg_value_filler(repo_root, TARGET_ITEM_COUNT - items.len());
    for item in fill.drain(..) {
      if !items.iter().any(|existing| existing.id == item.id) {
        items.push(item);
      }
      if items.len() >= TARGET_ITEM_COUNT {
        break;
      }
    }
    items.sort_by_key(|item| (item.tier, item.why.is_none(), item.id.clone()));
  }

  Ok(items)
}

