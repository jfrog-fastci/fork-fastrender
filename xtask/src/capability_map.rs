fn alg_group(
  group: &str,
  tier: PriorityTier,
  specs: &[&str],
  entries: &[(&str, &str)],
) -> Vec<CapabilityItem> {
  let specs: Vec<String> = specs.iter().map(|s| (*s).to_string()).collect();
  entries
    .iter()
    .map(|(id, title)| CapabilityItem {
      id: format!("alg.{}.{}", group, id),
      title: title.to_string(),
      tier,
      why: None,
      specs: specs.clone(),
      evidence: Vec::new(),
      group: Some(format!("alg.{}", group)),
    })
    .collect()
}

