/// Maximum size (in bytes) for a single IPC message payload.
///
/// This is a hard safety cap to prevent untrusted peers from forcing unbounded allocations in the
/// browser/renderer IPC layer.
///
/// Note: pixel buffers can be large (e.g. 1080p RGBA8 is ~8.3 MiB). The long-term plan is to move
/// large frame transfers to shared memory, but for early development we allow moderately-sized
/// inline buffers.
pub const MAX_IPC_MESSAGE_BYTES: usize = 64 * 1024 * 1024; // 64 MiB

/// Hard cap on navigation URL string length (in bytes) when deriving site keys.
///
/// This is a defense-in-depth limit: even though IPC framing already caps message sizes, a
/// compromised peer could still send multi-megabyte URL strings that would be expensive to parse
/// with the `url` crate and could lead to unbounded CPU usage.
///
/// When a URL exceeds this length, FastRender treats it as having an opaque origin for site
/// isolation purposes.
pub const MAX_SITE_KEY_URL_BYTES: usize = 8 * 1024;

/// Hard cap on how many subframes (iframes) a single frame may report for browser-side composition
/// or hit testing.
///
/// This is a defense-in-depth limit: the renderer process is untrusted and could otherwise attempt
/// to force the browser into `O(n log n)` sorts and `O(n)` hit-testing/compositing work on every
/// frame or input event.
pub const MAX_SUBFRAMES_PER_FRAME: usize = 256;

/// Hard cap on how many clip items may be supplied for a single subframe.
///
/// The browser-side compositor/hit tester walks the entire clip stack, so this cap bounds worst
/// case CPU usage if a compromised renderer tries to send a pathological clip stack.
pub const MAX_SUBFRAME_CLIP_STACK_DEPTH: usize = 64;

