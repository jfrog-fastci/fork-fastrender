/// Identifier for a frame (tab/iframe) hosted inside a renderer process.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash, Serialize, Deserialize)]
pub struct FrameId(pub u64);

impl FrameId {
  #[inline]
  pub const fn new(raw: u64) -> Self {
    Self(raw)
  }

  #[inline]
  pub const fn raw(self) -> u64 {
    self.0
  }
}

/// Stable identifier for a subframe element (e.g. an `<iframe>`) within its parent frame.
///
/// This token is generated by the *renderer* when it discovers iframe elements and must remain
/// stable across incremental layout/paint cycles and DOM mutations that update geometry or
/// navigation attributes (`src`, `srcdoc`) without removing the element.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash, Serialize, Deserialize)]
pub struct SubframeToken(pub u64);

/// Navigation target for an iframe browsing context.
///
/// This is intentionally a small "spec key" rather than a full fetch request: the browser needs
/// to:
/// - decide process assignment (site isolation) based on the target URL, and
/// - detect dynamic iframe navigations (`src`/`srcdoc` changes) without tearing down the frame tree.
///
/// For `srcdoc`, only a content hash is included: the actual HTML is expected to remain in the
/// embedding document's DOM and be read by the renderer process hosting the iframe.
#[derive(Debug, Clone, PartialEq, Eq, Hash, Serialize, Deserialize)]
pub enum IframeNavigation {
  /// Navigate to the given URL (already resolved against the parent base URL by the renderer).
  Url(String),
  /// Navigate to `about:blank` (used for missing/empty `src`).
  AboutBlank,
  /// Navigate to `about:srcdoc` with the provided content hash.
  Srcdoc { content_hash: u64 },
}

impl IframeNavigation {
  /// Returns the effective URL string used for site-key computation.
  #[inline]
  pub fn effective_url(&self) -> &str {
    match self {
      IframeNavigation::Url(url) => url.as_str(),
      IframeNavigation::AboutBlank => "about:blank",
      IframeNavigation::Srcdoc { .. } => "about:srcdoc",
    }
  }
}

/// Referrer policy applied when generating the `Referer` header for navigation/subresource requests.
///
/// This is intentionally aligned with FastRender's in-process [`crate::resource::ReferrerPolicy`]
/// enum so that browser/renderer processes can communicate the effective policy deterministically.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash, Serialize, Deserialize)]
pub enum ReferrerPolicy {
  /// Empty-string / unspecified referrer policy ("use default").
  EmptyString,
  NoReferrer,
  NoReferrerWhenDowngrade,
  Origin,
  OriginWhenCrossOrigin,
  SameOrigin,
  StrictOrigin,
  StrictOriginWhenCrossOrigin,
  UnsafeUrl,
}

impl Default for ReferrerPolicy {
  fn default() -> Self {
    Self::EmptyString
  }
}

impl ReferrerPolicy {
  /// Parse a referrer policy token (case-insensitive, trims ASCII whitespace).
  pub fn parse(raw: &str) -> Option<Self> {
    let token = raw.trim_matches(|c: char| {
      matches!(c, '\u{0009}' | '\u{000A}' | '\u{000C}' | '\u{000D}' | ' ')
    });
    if token.is_empty() {
      return Some(Self::EmptyString);
    }
    if token.eq_ignore_ascii_case("no-referrer") {
      Some(Self::NoReferrer)
    } else if token.eq_ignore_ascii_case("no-referrer-when-downgrade") {
      Some(Self::NoReferrerWhenDowngrade)
    } else if token.eq_ignore_ascii_case("origin") {
      Some(Self::Origin)
    } else if token.eq_ignore_ascii_case("origin-when-cross-origin") {
      Some(Self::OriginWhenCrossOrigin)
    } else if token.eq_ignore_ascii_case("same-origin") {
      Some(Self::SameOrigin)
    } else if token.eq_ignore_ascii_case("strict-origin") {
      Some(Self::StrictOrigin)
    } else if token.eq_ignore_ascii_case("strict-origin-when-cross-origin") {
      Some(Self::StrictOriginWhenCrossOrigin)
    } else if token.eq_ignore_ascii_case("unsafe-url") {
      Some(Self::UnsafeUrl)
    } else {
      None
    }
  }

  /// Parse a `referrerpolicy` attribute value.
  ///
  /// Returns `None` when the attribute is missing, empty, or invalid, which signals that the
  /// request should use the document's default referrer policy.
  pub fn from_attribute(value: &str) -> Option<Self> {
    match Self::parse(value)? {
      Self::EmptyString => None,
      other => Some(other),
    }
  }
}

/// Parsed sandbox flags for an `<iframe sandbox>` attribute.
///
/// This is a conservative subset of the HTML sandboxing flags, represented as "allow-*" bits. The
/// empty value means the sandbox attribute is present with no allowances (maximum restrictions).
#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash, Serialize, Deserialize, Default)]
pub struct SandboxFlags(pub u32);

impl SandboxFlags {
  pub const NONE: Self = Self(0);
  pub const ALLOW_SAME_ORIGIN: Self = Self(1 << 0);
  pub const ALLOW_SCRIPTS: Self = Self(1 << 1);
  pub const ALLOW_FORMS: Self = Self(1 << 2);
  pub const ALLOW_POPUPS: Self = Self(1 << 3);
  pub const ALLOW_TOP_NAVIGATION: Self = Self(1 << 4);

  #[inline]
  pub const fn contains(self, other: Self) -> bool {
    (self.0 & other.0) == other.0
  }

  #[inline]
  pub fn insert(&mut self, other: Self) {
    self.0 |= other.0;
  }

  #[inline]
  pub const fn is_empty(self) -> bool {
    self.0 == 0
  }
}

/// Origin-like tuple used to scope documents to renderer processes.
///
/// This is used by the browser to deterministically convey:
/// - inherited origins (about:blank, srcdoc), and
/// - opaque origins (sandboxed, data:, etc.)
#[derive(Debug, Clone, PartialEq, Eq, Hash, Serialize, Deserialize)]
pub struct DocumentOrigin {
  pub scheme: String,
  pub host: Option<String>,
  pub port: Option<u16>,
}

impl DocumentOrigin {
  /// Construct an origin tuple from a parsed URL, normalizing default HTTP(S) ports.
  pub fn from_url(url: &Url) -> Self {
    let scheme = url.scheme().to_ascii_lowercase();
    let host = url.host_str().map(|h| h.to_ascii_lowercase());
    let port = match scheme.as_str() {
      "http" | "https" => url.port_or_known_default(),
      _ => url.port(),
    };
    Self { scheme, host, port }
  }

  /// Parse an origin tuple from a URL string.
  pub fn from_url_str(url: &str) -> Option<Self> {
    let parsed = Url::parse(url).ok()?;
    Some(Self::from_url(&parsed))
  }
}

impl std::fmt::Display for DocumentOrigin {
  fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
    if self.scheme == "file" {
      return write!(f, "file://");
    }
    let host = self.host.as_deref().unwrap_or("<unknown>");
    let host = if host.contains(':') && !host.starts_with('[') {
      format!("[{host}]")
    } else {
      host.to_string()
    };
    match self.port {
      Some(port) => write!(f, "{}://{}:{}", self.scheme, host, port),
      None => write!(f, "{}://{}", self.scheme, host),
    }
  }
}

/// Site isolation key used by the browser to decide which renderer process hosts a frame.
#[derive(Debug, Clone, PartialEq, Eq, Hash, Serialize, Deserialize)]
pub enum SiteKey {
  Origin(DocumentOrigin),
  /// Unique per-document opaque origin key.
  Opaque(u64),
}

impl Default for SiteKey {
  fn default() -> Self {
    // Keep `0` reserved as an "invalid/unspecified" opaque key.
    Self::Opaque(0)
  }
}

/// How renderer processes are locked to sites/origins.
///
/// This is used to derive a [`SiteLock`] from a [`SiteKey`] when spawning a renderer process.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash, Serialize, Deserialize)]
pub enum SiteIsolationMode {
  /// Lock a renderer process to a full origin (scheme + host + port).
  PerOrigin,
  /// Lock a renderer process to a schemeful site (scheme + registrable domain).
  PerSite,
}

/// Defense-in-depth lock enforced by a renderer process.
///
/// The browser assigns one `SiteLock` per renderer process and the renderer must reject any
/// navigation whose [`SiteKey`] does not match this lock.
#[derive(Debug, Clone, PartialEq, Eq, Hash, Serialize, Deserialize)]
pub enum SiteLock {
  /// Full origin lock (scheme/host/port).
  Origin(DocumentOrigin),
  /// Schemeful site lock: scheme + registrable domain (eTLD+1).
  SchemefulSite { scheme: String, registrable_domain: String },
  /// Opaque lock for documents with opaque origins.
  Opaque(u64),
}

impl SiteLock {
  fn registrable_domain(host: &str) -> String {
    static PSL: OnceLock<List> = OnceLock::new();
    let list = PSL.get_or_init(List::default);
    let host = host.trim_end_matches('.').to_ascii_lowercase();

    // IP literals do not have a registrable domain; treat the full host as the site key so we do
    // not accidentally group unrelated IPs together (e.g. `127.0.0.1` vs `192.0.2.1`).
    if host.parse::<std::net::IpAddr>().is_ok() {
      return host;
    }

    let Some(suffix) = list.suffix(host.as_bytes()) else {
      // If we cannot derive a public suffix, fall back to host-level isolation. This may create more
      // processes than necessary, but avoids over-grouping unrelated sites.
      return host;
    };
    let Ok(suffix_str) = std::str::from_utf8(suffix.as_bytes()) else {
      return host;
    };
    let mut suffix_labels = suffix_str.split('.').filter(|part| !part.is_empty()).count();
    let host_labels: Vec<&str> = host.split('.').filter(|part| !part.is_empty()).collect();
    if host_labels.len() <= suffix_labels {
      return host;
    }

    // The bundled public suffix list used by `publicsuffix::List::default()` is intentionally
    // lightweight and does not include all multi-level public suffix rules. To avoid over-grouping
    // common ccTLD registries (e.g. `example.co.uk` vs `other.co.uk`) we apply a conservative
    // heuristic:
    //
    // - When the computed suffix is a 2-letter ccTLD (e.g. `uk`, `au`), and the label immediately
    //   preceding it is a common second-level registry bucket (`co`, `com`, `net`, ...), treat the
    //   *pair* as the effective public suffix (e.g. `co.uk`).
    //
    // This can only make the derived site *more specific* (less sharing), which is safe for site
    // isolation. It may increase process churn in some edge cases but avoids grouping unrelated
    // sites under one ccTLD bucket.
    if suffix_labels == 1 && suffix_str.len() == 2 && host_labels.len() >= 3 {
      let second_last = host_labels[host_labels.len() - 2];
      if matches!(
        second_last,
        "ac" | "co" | "com" | "edu" | "gov" | "net" | "org"
      ) {
        suffix_labels = 2;
      }
    }

    // Registered domain (eTLD+1) = public suffix + one additional label.
    let start = host_labels.len().saturating_sub(suffix_labels + 1);
    host_labels[start..].join(".")
  }

  /// Derive a renderer `SiteLock` from the browser-assigned [`SiteKey`].
  pub fn from_site_key(site_key: &SiteKey, mode: SiteIsolationMode) -> Self {
    match site_key {
      SiteKey::Opaque(id) => SiteLock::Opaque(*id),
      SiteKey::Origin(origin) => match mode {
        SiteIsolationMode::PerOrigin => SiteLock::Origin(origin.clone()),
        SiteIsolationMode::PerSite => match origin.host.as_deref() {
          Some(host) => SiteLock::SchemefulSite {
            scheme: origin.scheme.clone(),
            registrable_domain: Self::registrable_domain(host),
          },
          None => SiteLock::Origin(origin.clone()),
        },
      },
    }
  }

  /// True when `site_key` is permitted to commit inside this locked renderer process.
  pub fn matches_site_key(&self, site_key: &SiteKey) -> bool {
    match (self, site_key) {
      (SiteLock::Opaque(a), SiteKey::Opaque(b)) => a == b,
      (SiteLock::Origin(a), SiteKey::Origin(b)) => a == b,
      (
        SiteLock::SchemefulSite {
          scheme,
          registrable_domain,
        },
        SiteKey::Origin(origin),
      ) => {
        origin.scheme == *scheme
          && origin
            .host
            .as_deref()
            .is_some_and(|host| Self::registrable_domain(host) == *registrable_domain)
      }
      _ => false,
    }
  }

  /// True when navigating to `url` is permitted to commit inside this locked renderer process.
  ///
  /// This is a defense-in-depth check: it derives the site/origin from the URL itself (where
  /// possible) rather than trusting the browser-provided [`SiteKey`] inside
  /// [`NavigationContext`].
  ///
  /// For inheriting/opaque URLs like `about:blank`, the renderer cannot derive the effective origin
  /// from the URL alone. In those cases, it falls back to validating the browser-provided
  /// `claimed_site_key`.
  pub fn matches_url(&self, url: &str, claimed_site_key: &SiteKey) -> bool {
    match self {
      SiteLock::Opaque(_) => return self.matches_site_key(claimed_site_key),
      SiteLock::Origin(_) | SiteLock::SchemefulSite { .. } => {}
    }

    if let Some(about_path) = url.strip_prefix("about:") {
      if about_path.eq_ignore_ascii_case("blank") || about_path.eq_ignore_ascii_case("srcdoc") {
        return self.matches_site_key(claimed_site_key);
      }
    }

    // Blob URLs embed the origin in the payload: `blob:https://example.com/...`.
    let url_for_origin = url.strip_prefix("blob:").unwrap_or(url);
    let Ok(parsed) = Url::parse(url_for_origin) else {
      return false;
    };

    match self {
      SiteLock::Origin(lock_origin) => DocumentOrigin::from_url(&parsed) == *lock_origin,
      SiteLock::SchemefulSite {
        scheme,
        registrable_domain,
      } => {
        if parsed.scheme() != scheme {
          return false;
        }
        let Some(host) = parsed.host_str() else {
          return false;
        };
        Self::registrable_domain(host) == *registrable_domain
      }
      SiteLock::Opaque(_) => self.matches_site_key(claimed_site_key),
    }
  }
}

/// Generator for [`SiteKey::Opaque`] values.
///
/// Browsers treat some navigations (e.g. `data:` and most `about:`) as having an opaque origin.
/// For site isolation those documents must not share renderer processes with other origins, so the
/// browser assigns them unique `SiteKey::Opaque` values.
#[derive(Debug)]
pub struct SiteKeyFactory {
  next_opaque_id: AtomicU64,
  file_url_isolation: FileUrlSiteIsolation,
}

impl Default for SiteKeyFactory {
  fn default() -> Self {
    Self::new_with_seed(1)
  }
}

impl SiteKeyFactory {
  /// Create a new factory whose first generated opaque ID will be `seed`.
  pub const fn new_with_seed(seed: u64) -> Self {
    Self::new_with_seed_and_file_url_isolation(seed, FileUrlSiteIsolation::OpaquePerUrl)
  }

  /// Create a new factory with an explicit `file:` URL isolation mode.
  pub const fn new_with_seed_and_file_url_isolation(
    seed: u64,
    file_url_isolation: FileUrlSiteIsolation,
  ) -> Self {
    Self {
      next_opaque_id: AtomicU64::new(seed),
      file_url_isolation,
    }
  }

  fn new_opaque(&self) -> SiteKey {
    let id = self.next_opaque_id.fetch_add(1, Ordering::Relaxed);
    SiteKey::Opaque(id)
  }

  fn file_origin() -> &'static DocumentOrigin {
    static FILE_ORIGIN: OnceLock<DocumentOrigin> = OnceLock::new();
    FILE_ORIGIN.get_or_init(|| DocumentOrigin {
      scheme: "file".to_string(),
      host: None,
      port: None,
    })
  }

  fn stable_file_hash_u64(&self, bytes: &[u8]) -> u64 {
    // Domain-separated hash so `file:`-derived opaque IDs don't accidentally collide with other
    // hashes should we introduce them in the future.
    let mut hasher = Sha256::new();
    hasher.update(b"fastrender:file-site-key:v1\0");
    hasher.update(bytes);
    let digest = hasher.finalize();

    // Use 64 bits (with a tag bit) since `SiteKey::Opaque` is a u64 today.
    let mut first8 = [0u8; 8];
    first8.copy_from_slice(&digest[..8]);
    let raw = u64::from_le_bytes(first8);

    // Tag file-derived opaque IDs into the top half of the u64 space so they won't collide with the
    // sequential `new_opaque()` IDs unless a session performs ~2^63 opaque navigations.
    raw | (1u64 << 63)
  }

  fn site_key_for_file_url(&self, parsed: &Url) -> SiteKey {
    match self.file_url_isolation {
      FileUrlSiteIsolation::SingleSite => SiteKey::Origin(Self::file_origin().clone()),
      FileUrlSiteIsolation::OpaquePerUrl => {
        if let Ok(path) = parsed.to_file_path() {
          let id = self.stable_file_hash_u64(path.as_os_str().to_string_lossy().as_bytes());
          SiteKey::Opaque(id)
        } else {
          let mut canonical = parsed.clone();
          canonical.set_fragment(None);
          canonical.set_query(None);
          let id = self.stable_file_hash_u64(canonical.as_str().as_bytes());
          SiteKey::Opaque(id)
        }
      }
      FileUrlSiteIsolation::OpaquePerDirectory => {
        if let Ok(path) = parsed.to_file_path() {
          let dir = path.parent().unwrap_or(Path::new(""));
          let id = self.stable_file_hash_u64(dir.as_os_str().to_string_lossy().as_bytes());
          SiteKey::Opaque(id)
        } else {
          // If we can't map the URL into a platform file path, fall back to per-URL hashing.
          let mut canonical = parsed.clone();
          canonical.set_fragment(None);
          canonical.set_query(None);
          let id = self.stable_file_hash_u64(canonical.as_str().as_bytes());
          SiteKey::Opaque(id)
        }
      }
    }
  }

  /// Derive the site key for a navigation, optionally inheriting from a parent.
  ///
  /// Rules:
  /// - HTTP(S): key by origin (scheme/host/port).
  /// - `about:blank` / `about:srcdoc`: inherit `parent` when provided; otherwise create a new
  ///   opaque key.
  /// - `data:`: always opaque.
  /// - Unparseable/unsupported URLs: opaque.
  pub fn site_key_for_navigation(&self, url: &str, parent: Option<&SiteKey>) -> SiteKey {
    // Avoid pathological URL strings causing expensive parsing work.
    if url.len() > MAX_SITE_KEY_URL_BYTES {
      return self.new_opaque();
    }

    // `blob:` URLs (e.g. `blob:https://example.com/uuid`) inherit their origin from the embedded
    // URL. Treat same-origin blob navigations as the same `SiteKey` to avoid unnecessary process
    // churn for `URL.createObjectURL()` results.
    if url
      .as_bytes()
      .get(..5)
      .is_some_and(|head| head.eq_ignore_ascii_case(b"blob:"))
    {
      // Blob URL origin is derived from the origin of the embedded URL. If the embedded URL is
      // `null` or unparseable (`blob:null/...`), treat it as opaque.
      let embedded = &url[5..];
      let Ok(parsed_embedded) = Url::parse(embedded) else {
        return self.new_opaque();
      };

      // Blob origins do not inherit from the navigating frame; only `about:blank` / `about:srcdoc`
      // do. Derive the site key solely from the embedded URL.
      return match parsed_embedded.scheme() {
        "http" | "https" => {
          if parsed_embedded.host_str().is_none() {
            self.new_opaque()
          } else {
            SiteKey::Origin(DocumentOrigin::from_url(&parsed_embedded))
          }
        }
        "file" => self.site_key_for_file_url(&parsed_embedded),
        "about" => self.new_opaque(),
        "data" => self.new_opaque(),
        _ => self.new_opaque(),
      };
    }

    let parsed = match Url::parse(url) {
      Ok(parsed) => parsed,
      Err(_) => return self.new_opaque(),
    };

    match parsed.scheme() {
      "http" | "https" => {
        if parsed.host_str().is_none() {
          return self.new_opaque();
        }
        SiteKey::Origin(DocumentOrigin::from_url(&parsed))
      }
      "file" => self.site_key_for_file_url(&parsed),
      "about" => {
        let path = parsed.path();
        if path.eq_ignore_ascii_case("blank") || path.eq_ignore_ascii_case("srcdoc") {
          parent.cloned().unwrap_or_else(|| self.new_opaque())
        } else {
          self.new_opaque()
        }
      }
      "data" => self.new_opaque(),
      _ => self.new_opaque(),
    }
  }

  /// Derive the site key for a subframe navigation, taking sandboxed opaque-origins into account.
  ///
  /// When `opaque_origin` is true (e.g. `<iframe sandbox>` without `allow-same-origin`), the child
  /// must be treated as having a unique origin even if it would otherwise inherit its parent.
  pub fn site_key_for_subframe_navigation(
    &self,
    url: &str,
    parent: Option<&SiteKey>,
    opaque_origin: bool,
  ) -> SiteKey {
    if opaque_origin {
      return self.new_opaque();
    }
    self.site_key_for_navigation(url, parent)
  }
}

/// Derive the site key for a navigation using a shared global factory.
pub fn site_key_for_navigation(url: &str, parent: Option<&SiteKey>) -> SiteKey {
  static FACTORY: OnceLock<SiteKeyFactory> = OnceLock::new();
  FACTORY
    .get_or_init(SiteKeyFactory::default)
    .site_key_for_navigation(url, parent)
}

/// How `file:` URLs are mapped into [`SiteKey`]s.
///
/// Real browsers treat `file:` documents as having opaque origins; for site isolation we need a
/// policy that avoids co-hosting unrelated local files in the same renderer process.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash, Serialize, Deserialize)]
pub enum FileUrlSiteIsolation {
  /// Legacy behaviour: all `file:` URLs share one site bucket.
  ///
  /// This is less secure but can reduce process churn in tests/harnesses that load many local
  /// fixtures.
  SingleSite,
  /// Derive an opaque key from the full absolute file path (stable for a given file URL).
  ///
  /// Same file URL ⇒ same `SiteKey`
  /// Different file paths ⇒ different `SiteKey`
  OpaquePerUrl,
  /// Derive an opaque key from the parent directory of the file path (stable per directory).
  ///
  /// Files in the same directory share a `SiteKey`; files in different directories do not.
  OpaquePerDirectory,
}

impl Default for FileUrlSiteIsolation {
  fn default() -> Self {
    Self::OpaquePerUrl
  }
}

