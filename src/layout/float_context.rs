/// Side-based clearing directive resolved into the float layout coordinate system.
///
/// `FloatContext` operates in a logical coordinate system where the inline-start side is
/// represented by `Left` and the inline-end side by `Right` (regardless of `direction`).
#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash, Default)]
pub enum ClearSide {
  /// Do not clear floats.
  #[default]
  None,
  /// Clear floats on the inline-start side.
  Left,
  /// Clear floats on the inline-end side.
  Right,
  /// Clear floats on both inline-start and inline-end sides.
  Both,
}

impl ClearSide {
  #[inline]
  pub fn is_clearing(self) -> bool {
    !matches!(self, ClearSide::None)
  }

  #[inline]
  pub fn clears_left(self) -> bool {
    matches!(self, ClearSide::Left | ClearSide::Both)
  }

  #[inline]
  pub fn clears_right(self) -> bool {
    matches!(self, ClearSide::Right | ClearSide::Both)
  }
}

/// Side on which a float is placed
///
/// This enum represents the side of the *inline axis* where a float is positioned in the
/// [`FloatContext`] coordinate system.
///
/// `FloatSide::Left` corresponds to **inline-start**, and `FloatSide::Right` corresponds to
/// **inline-end**. For `writing-mode: horizontal-tb` this matches physical left/right in LTR, but is
/// reversed in RTL.
#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash)]
pub enum FloatSide {
  /// Float is positioned on the inline-start side.
  Left,
  /// Float is positioned on the inline-end side.
  Right,
}

/// Resolves a CSS `float` value into a side in the float layout coordinate system.
///
/// The returned side is *flow-relative* (inline-start/end). Physical `left`/`right` are mapped
/// into flow-relative sides using the provided `writing_mode`/`direction`.
pub(crate) fn resolve_float_side(
  float: Float,
  writing_mode: WritingMode,
  direction: Direction,
) -> Option<FloatSide> {
  match float {
    Float::None | Float::Footnote => None,
    Float::InlineStart => Some(FloatSide::Left),
    Float::InlineEnd => Some(FloatSide::Right),
    Float::Left | Float::Right => {
      // In CSS Writing Modes, `left`/`right` in properties like `float` are line-relative
      // (`line-left`/`line-right`), not tied to the element's inline-start/inline-end. In vertical
      // writing modes this maps to physical top/bottom, so `direction` must be consulted to map
      // the physical line side back into the flow-relative inline sides used by `FloatContext`.
      let inline_positive = inline_axis_positive(writing_mode, direction);
      match float {
        // physical left == inline-start when inline axis is positive, otherwise inline-end.
        Float::Left => Some(if inline_positive {
          FloatSide::Left
        } else {
          FloatSide::Right
        }),
        // physical right == inline-end when inline axis is positive, otherwise inline-start.
        Float::Right => Some(if inline_positive {
          FloatSide::Right
        } else {
          FloatSide::Left
        }),
        _ => {
          debug_assert!(false, "resolve_float_side: matched Float::Left|Right above");
          Some(FloatSide::Left)
        }
      }
    }
  }
}

/// Resolves a CSS `clear` value into a `ClearSide` in the float layout coordinate system.
pub(crate) fn resolve_clear_side(
  clear: crate::style::float::Clear,
  writing_mode: WritingMode,
  direction: Direction,
) -> ClearSide {
  use crate::style::float::Clear as CssClear;

  match clear {
    CssClear::None => ClearSide::None,
    CssClear::Both => ClearSide::Both,
    CssClear::InlineStart => ClearSide::Left,
    CssClear::InlineEnd => ClearSide::Right,
    CssClear::Left | CssClear::Right => {
      let inline_positive = inline_axis_positive(writing_mode, direction);
      match clear {
        CssClear::Left => {
          if inline_positive {
            ClearSide::Left
          } else {
            ClearSide::Right
          }
        }
        CssClear::Right => {
          if inline_positive {
            ClearSide::Right
          } else {
            ClearSide::Left
          }
        }
        _ => {
          debug_assert!(false, "resolve_clear_side: matched Clear::Left|Right above");
          ClearSide::None
        }
      }
    }
  }
}

/// Information about a single float
///
/// Stores the position and dimensions of a floated box within a BFC.
/// All coordinates are relative to the BFC's origin.
#[derive(Debug, Clone, PartialEq)]
pub struct FloatInfo {
  /// The side on which this float is placed
  pub side: FloatSide,

  /// The float's bounding rectangle (margin box)
  ///
  /// Coordinates are relative to the containing block formatting context.
  /// This is the margin box of the float, including margins.
  pub rect: Rect,

  /// Optional wrapping shape from `shape-outside`.
  pub shape: Option<Arc<FloatShape>>,
}

impl FloatInfo {
  /// Create a new float info
  pub fn new(side: FloatSide, rect: Rect) -> Self {
    Self {
      side,
      rect,
      shape: None,
    }
  }

  pub fn new_with_shape(side: FloatSide, rect: Rect, shape: Option<Arc<FloatShape>>) -> Self {
    Self { side, rect, shape }
  }

  /// Create an inline-start float at the given position.
  pub fn new_left(x: f32, y: f32, width: f32, height: f32) -> Self {
    Self {
      side: FloatSide::Left,
      rect: Rect::from_xywh(x, y, width, height),
      shape: None,
    }
  }

  /// Create an inline-end float at the given position.
  pub fn new_right(x: f32, y: f32, width: f32, height: f32) -> Self {
    Self {
      side: FloatSide::Right,
      rect: Rect::from_xywh(x, y, width, height),
      shape: None,
    }
  }

  /// Returns the top edge of the float
  pub fn top(&self) -> f32 {
    self.rect.y()
  }

  /// Returns the bottom edge of the float (y + height)
  pub fn bottom(&self) -> f32 {
    self.rect.y() + self.rect.height()
  }

  /// Returns the left edge of the float
  pub fn left_edge(&self) -> f32 {
    self.rect.x()
  }

  /// Returns the right edge of the float (x + width)
  pub fn right_edge(&self) -> f32 {
    self.rect.x() + self.rect.width()
  }

  /// Returns true if the float overlaps vertically with a given y range
  pub fn overlaps_y_range(&self, y_start: f32, y_end: f32) -> bool {
    // Float overlaps if it starts before y_end and ends after y_start
    self.top() < y_end && self.bottom() > y_start
  }

  /// Returns true if the float contains the given y coordinate
  pub fn contains_y(&self, y: f32) -> bool {
    y >= self.top() && y < self.bottom()
  }
}

